  <h1>Charter</h1>
  <p>This coalition of neighbors and community organizers is committed to electing progressives to all positions in Town government to achieve the <b>boldly progressive</b> changes Brookline wants and needs. This here is the living document of our charter.</p>

  <h2>Our Values</h2>

  <p><q>Progressive</q> means different things to different people. To our organizers and candidates, progressive means taking real action to bring material, systemic change for racial, social, environmental, and economic justice.</p>

  <p>Progressive should mean — and has meant to our incumbent candidates — fighting for:</p>

  <h3>The improved health & wellbeing of residents</h3>

  <ul>
    <li><b>Fight food insecurity.</b> Use Town resources to do so when required.</li>
    <li><b>Fully fund services for older adults.</b> Ensure institutions like the Senior Center are amply staffed and resourced, so older adults can age comfortably and healthily.</li>
    <li><b>Refurbish and renovate public housing units.</b> All residents deserve healthy and safe living spaces.</li>
    <li><b>Ample resources for public school students and teachers.</b> And a commitment to make the Brookline public schools system more equitable, regardless of race, color, national origin and income.</li>
  </ul>

  <h3>Forward-looking, community-driven planning</h3>

  <ul>
    <li><b>Housing justice.</b> We need more affordable housing; not just any housing but more homes affordable to traditionally excluded people and older adults, family public housing, co-ops, and community land trusts.</li>
    <li><b>Charter change.</b> An openness to that process and other structural changes that would increase voter turnout and civic participation.</li>
    <li><b>Address the climate crisis.</b> Do so with the urgency, magnitude, and social justice lens it demands.</li>
    <li><b>Reimagining public safety.</b> Reallocate funds from the police budget to services that would better fulfill people’s social, health-related, and economic needs — that would, in turn, improve public safety.</li>
  </ul>

  <h3>Principled, open-minded leadership</h3>

  <ul>
    <li><b>Racial justice.</b> Our government should use a racial equity framework that clearly names Brookline’s history in creating and maintaining racial inequalities. Furthermore, we must acknowledge and provide restitution for the Town’s racist actions and history.</li>
    <li><b>Gender bias.</b> Women, as well as non-binary and genderfluid individuals, deserve proper levels of representation in all aspects of Town government.</li>
    <li><b>Intersectionality.</b> Pursue measures that reflect the interconnected nature of social and economic inequality, systems, and justice.</li>
    <li><b>LGBTQIA+ liberation.</b> Support policies, education, and actions that would combat heteronormativity, and promote healthy outlooks regarding sex, gender identity, and sexuality.</li>
    <li><b>Reproductive justice.</b> All people deserve unconstrained access to abortion care. Additionally, we must dismantle the additional barriers that people of color and low-income individuals face to contraception access.</li>
  </ul>

  <h3>Good governance</h3>

  <ul>
    <li><b>Inclusivity.</b> Support hybrid (in person and virtual) meeting options, and ensure residents have the technology for virtual access to meetings.</li>
    <li><b>Accessibility.</b> Better address the interests, concerns, and needs of those with physical, mental, and developmental disabilities or who have limited English proficiency.</li>
    <li><b>Representative.</b> Push for Town budgets that better reflect our Town’s progressive principles and priorities.</li>
    <li><b>Empower the unrepresented.</b> Center the voices of people closest to the issues. Make space for individuals from typically under-represented communities to not only have a seat at the table — but become leaders.</li>
  </ul>

  <h2>Our Plans</h2>

  <p>To achieve majorities in Town government willing to pass legislation in keeping with the values listed above, we intend to:</p>

  <ul>
    <li>Recruit and encourage like-minded neighbors to run for local office, plus support and mentor them in doing so.</li>
    <li>Particularly neighbors from traditionally less represented backgrounds, especially people of color and low-income neighbors.</li>
    <li>Organize and host social events to build and provide space for local community-building.</li>
    <li>Lay the foundations for a progressive caucus within Town government.</li>
  </ul>

  <h2>Our Process</h2>

  <p>In all our decisions both internal and external, we prioritize inclusion and accessibility, and strive to be efficient and democratic. Our intention is to make decisions and take actions that uphold our convictions, and which reflect actual political conditions.</p>

  <p><strong>If you are interested in being endorsed by us, please contact RBcampaignEmail@Gmail.com.</strong></p>

  <ul>
    <li>In your message, affirm that you too believe in the values described above.
      <ul>
        <li>Note that we will look at political contribution history, previous endorsements (of and by you), past public statements, and voting records (if you’re an incumbent).</li>
        <li>We understand that everyone’s politics can evolve, so rather than judge someone according to a single statement or decision, we will consider the totality of information available.</li>
        <li>Membership in our group does not obligate a candidate to mutually endorse all of our other candidates. <strong>However, given 1) the importance of the 15th Norfolk district State Rep race and 2) the fact that Raul Fernandez is by far the most progressive person in that race, our coalition is to be composed of only individuals who’ve endorsed his campaign.</strong></li>
      </ul>
    </li>
  </ul>

  <p><strong>Individuals who are not seeking endorsements can join our Coalition as an organizer.</strong></p>

  <ul>
    <li>To join as an organizer, send an email which affirms you accept our values as described above.
      <ul>
        <li>Note that we will look at political contribution history, previous endorsements (of and by you), past public statements, and voting records (if you did hold office previously).</li>
      </ul>
    </li>
  </ul>

  <p>We also feel an obligation to ensure our Coalition is an organizing space safe from those whose intentions run counter to the progressive values outlined above.</p>

  <ul>
    <li>Individuals who are known to be campaigning for conservative or reactionary candidates and causes, and/or undermining progressive efforts will not be permitted admittance.</li>
  </ul>

