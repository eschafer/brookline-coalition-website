  <h1>Accessibility Statement</h1>

  <p>The Brookline Equity Coalition is committed to ensuring accessibility across all media for <a id="fn1ref" href="#fn1" aria-label="Footnote 1" class="usa-link">people with disabilities*</a>.</p>

  <p>The best way to do this is to include people with disabilities in our organization. If you're interested, we'd love for you to <a href="/get-involved" class="usa-link">get involved</a>!</p>

  <p>We are continually improving the user experience for everyone, and applying the relevant accessibility standards. We want everyone to feel welcome and included ðŸ’œ.</p>

  <h2>How are we doing?</h2>

  <p>The <a href="https://www.w3.org/WAI/standards-guidelines/wcag/" class="usa-link usa-link--external">Web Content Accessibility Guidelines (WCAG)</a> defines requirements for designers and developers to improve accessibility for people with disabilities. It defines three levels of conformance: Level A, Level AA, and Level AAA.</p>

  <p>While we don't have the resources for thorough testing, <strong>we strive to be fully conformant with WCAG 2.1 level AA across all media</strong>.</p>

