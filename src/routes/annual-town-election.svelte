      <p>During Brookline's Tuesday, May 3rd municipal election <em>245 Town Meeting Member seats</em> will be on the ballot.</p>

      <dl>
        <dt>What is Town Meeting?</dt>
        <dd>Town Meeting is the assembly of residents who decide the legislative policies of Brookline.</dd>

        <dt>Why so many seats this year?</dt>
        <dd>The results of the most recent U.S. Census prompted a redistricting process where every precinct (except precinct 16) changed by at least one Census block. If a precinct was altered by even one Census block, all 15 of its Town Meeting Member seats are to be voted on at the next Town election.</dd>

        <dt>Wait, redistricting happened?</dt>
        <dd>Indeed! Here is a <a href="https://www.brooklinema.gov/DocumentCenter/View/26965/Brookline-Precinct-Map-2022" class="usa-link usa-link--extended">map of Brookline's new precincts and polling locations (PDF)</a>. Brookline grew from 16 to 17 precincts, the new 17th precinct being centered around Coolidge Corner.</dd>

        <dt>Where can I register to vote?</dt>
        <dd><a href="https://www.sec.state.ma.us/OVR/Pages/CheckEligibility.aspx?&Action=Register" class="usa-link usa-link--external">Register to vote</a> on the Massachusetts Secretary of State's website.</dd>

        <dt>Can I run for Town Meeting?</dt>
        <dd>
          <p>If you're a Brookline voter, yes! You can run to be one of your precinct's 15 Town Meeting Members.</p>
          <p>Town Meetings, where legislation is debated and voted on, occur twice a year; occasionally there is an additional Special Town Meeting. The Spring (May/June) and Fall (November/December) sessions each last about 4-9 evenings, depending on the number of issues to vote on.</p>
          <p>The duration of your term may be one, two, or three years — and depends on whether you finish 1st-5th, 6th-10th, or 11th-15th in your precinct during the May election.</p>
          <p>The nomination period is now open! Candidates for Town Meeting can pick up nomination papers by appointment at the <a href="https://www.brooklinema.gov/131/Town-Clerk" class="usa-link usa-link--external">Town Clerk's Office</a> (<a href="tel:+16177302010" class="usa-link">617-730-2010</a>).</p>
          <p>Town Meeting Member nomination papers require 10 certified signatures from the precinct in which the candidate is running. <strong>The deadline to file for incumbents is March 8th at 5pm, and the deadline to file for new TMM candidates is March 15th at 5pm.</strong></p>
          <p>Furthermore, if you're interested in being endorsed by the Brookline Equity Coalition, <a href="https://docs.google.com/forms/d/e/1FAIpQLSccRojH6z-aqGKuD9vOuqUQ7I0T-C0eVtH4xcHMhjlSILDXIQ/viewform" class="usa-link usa-link--external">fill out this Google Form</a>.</p>
        </dd>
